// switch

let semaforo = "Nada";

switch(semaforo){
    case "Verde" :
        console.log("Puedo Cruzar");
        break;
    case "Amarillo" :
        console.log("Precaucion");
    case "Rojo" :
        console.log("No Cruze");
        break;
    default :
        console.log ("Error") 
}