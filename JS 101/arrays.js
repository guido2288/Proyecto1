let notas = [3, 5, 10, 9, 7, 8, 8, 8];

notas.push(1); //El metodo push agrega un elemento al final del Array


notas.pop() //El metodo pop quita siempre el ultimo elemento

notas.shift(); //El metodo shift quita siempre el primer elemento

notas.unshift(1); //El metodo unshift agrega un elemento al principio del array

console.log(notas) 